public class Parent2 {

    String name;
    int age;
    String gender;

    public Parent2(){
        this.name = "";
        this.age = 0;
        this.gender = "";
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }
}
